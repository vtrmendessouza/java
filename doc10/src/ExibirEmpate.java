public class ExibirEmpate {

    static void exibirEmpate() {
        System.out.println("Ocorreu empate!");
        System.out.println("+---+       +---+");
        System.out.println("| 0 |   X   | 0 |");
        System.out.println("+---+       +---+");
    }
}
/*
Descrição: Utilizado para exibir a frase: Ocorreu empate!, e uma ART ASCII 
do placar 0 X 0. Este método é utilizado quando é identificado que ocorreu empate. 
Lembre-se que para imprimir uma contrabara \ é necessário duas contra barras \\
Nível Complexidade: 2 de 10
*/