//03: Faça um algoritmo utilizando a estrutura "for" que imprima na tela os números de 1 à 10.
public class Exe03 {
    public static void main(String[] args) throws Exception {

        int q = 0;
        for(int i = 0; i < 10; i++){
            q++;
            System.out.printf("%d\n",q);
        }
    }
}